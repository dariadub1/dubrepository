using System;

class Program
{
    static void Main()
    {
        int N = 5; // Размерность 5 x 5
        int[,] array = new int[,]
        {
            {  1,  2,  3,  4,  5 },
            { 16, 17, 18, 19,  6 },
            { 15, 24, 25, 20,  7 },
            { 14, 23, 22, 21,  8 },
            { 13, 12, 11, 10,  9 }
        };

        // Сначала находим центр массива
        int centerX = N / 2;
        int centerY = N / 2;

        // Направления обхода - вправо, вниз, влево, вверх
        int[,] directions = { { 0, -1 }, { 1, 0 }, { 0, 1 }, { -1, 0 } };
        int dir = 0; // Начнем с направления вправо
        int steps = 1; // Количество шагов в текущем направлении
        int x = centerX, y = centerY; // Начальная позиция (центр)

        Console.WriteLine("Элементы массива в спиральном порядке, начиная с центра:");

        Console.Write(array[x, y] + " "); // Печатаем центральный элемент

        for (int i = 0; i < N * N - 1; i++) // Уменьшаем на 1, так как центральный элемент уже выведен
        {
            // Двигаемся в текущем направлении
            for (int j = 0; j < steps; j++)
            {
                x += directions[dir, 0];
                y += directions[dir, 1];

                // Проверяем, не выходит ли за пределы
                if (x >= 0 && x < N && y >= 0 && y < N)
                {
                    Console.Write(array[x, y] + " ");
                }
            }

            // Если достигли конца текущего количества шагов, изменяем направление
            dir = (dir + 1) % 4; // Меняем направление
            if (dir % 2 == 0) // Увеличиваем количество шагов после завершения полного оборота
            {
                steps++;
            }
        }
    }
}
